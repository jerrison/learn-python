print(f"locations {locations}")
print(f"sorted list: {sorted(locations)}")
print(
    f"locations, again: {locations}, notice that the list goes back to its original order"
)

locations.reverse()
print(f"locations, reversed: {locations}")
print(
    f"checking locations now: {locations}, notice that the order of the locations is changed permanently"
)

